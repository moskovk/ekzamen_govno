# Функция для вычисления определителя матрицы 2x2 с использованием метода Гаусса
function determinant_2x2(a::Float64, b::Float64, c::Float64, d::Float64)
    # Создаем 2x2 матрицу из введенных чисел
    matrix = [a b; c d]

    # Начальное значение определителя
    det = 1.0

    # Приведение матрицы к ступенчатому виду
    # Этап 1: Приводим элемент matrix[1, 1] к 1 (если он не ноль)
    if matrix[1, 1] == 0
        if matrix[2, 1] != 0
            # Меняем строки местами, если первый элемент первого ряда равен нулю, а первый элемент второго ряда не равен нулю
            matrix[1, :], matrix[2, :] = matrix[2, :], matrix[1, :]
            det *= -1 # При смене строк меняем знак определителя
        else
            # Если оба элемента в первом столбце равны нулю, определитель равен нулю
            return 0.0
        end
    end

    # Этап 2: Приводим нижний элемент первого столбца к нулю
    if matrix[2, 1] != 0
        factor = matrix[2, 1] / matrix[1, 1]
        matrix[2, :] -= factor * matrix[1, :]
    end

    # Этап 3: Приводим элемент matrix[2, 2] к 1
    if matrix[2, 2] == 0
        return 0.0 # Если на диагонали ноль, определитель равен нулю
    end

    # Этап 4: Вычисляем определитель как произведение диагональных элементов
    for i in 1:2
        det *= matrix[i, i]
    end

    return det
end

# Тестирование функции
a, b, c, d = 1.0, 2.0, 3.0, 4.0
det = determinant_2x2(a, b, c, d)
println("Определитель матрицы [[$a, $b], [$c, $d]] равен $det")
